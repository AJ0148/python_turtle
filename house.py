import turtle as t

t.shape("turtle")
t.pencolor("green")
t.color("green")

while True:
    t.fd(100)
    t.left(90)
    if abs(t.pos()) < 1:
        break

t.penup()
t.fd(50)
t.pendown()
t.left(90)
t.fd(50)
t.left(90)
t.fd(50)
t.penup()
t.left(180)
t.fd(50)
t.pendown()
t.fd(50)
t.penup()
t.left(180)
t.fd(50)
t.right(90)
t.pendown()
t.fd(50)
t.right(90)
t.penup()
t.fd(50)
t.left(135)
t.pencolor("black")
t.color("black")
t.pendown()
t.begin_fill()
t.fd(70)
t.left(90)
t.fd(70)
t.left(135)
t.fd(100)
t.end_fill()
t.hideturtle()

t.exitonclick()